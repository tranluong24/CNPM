    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
     Date batDau = new Date();
     Date ketThuc = new Date();
    try{
        String ngay = dateFormat.format(this.ngayBDText.getDate());
        String ngay2 = dateFormat.format(this.ngayKTText.getDate());
        batDau = dateFormat.parse(ngay);
        ketThuc = dateFormat.parse(ngay2);
    }catch(Exception e){
        JOptionPane.showMessageDialog(this, "Sai định dạng ngày");  
        return;
    }
        
        
        
        TamTruTamVangModel kb = new TamTruTamVangModel();

        String CCCD = new String();
        //String hoTen = new String();
        String hoTen = new String();
        String loaiDangKi = new String ();
        String diaDiem = new String();
        String lido = new String();

        if(this.CCCDText.getText().equals("")
                || this.hoTenText.getText().equals("")
                || this.chuHoText.getText().equals("")
                || this.diaDiemText.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin !");
        }else{
            try{
                CCCD = this.CCCDText.getText();
                loaiDangKi = this.loaiBox.getSelectedItem()+"";
                lido = this.lidoText.getText();
                if(loaiDangKi.equals("Tạm trú")){
                    diaDiem = diaDiemText.getText();
                }else{
                    diaDiem = this.chuyenDenText.getText();
                }
                hoTen = this.hoTenText.getText();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "Vui lòng điền đúng định dạng !");
                return;
            }

            kb.setCCCD(CCCD);
            kb.setBatDau(batDau);
            kb.setKetThuc(ketThuc);
            kb.setLoaiDangKy(loaiDangKi);
            kb.setLido(lido);
            kb.setDiaDiem(diaDiem);
            kb.setName(hoTen);

            //if(option.equals("add")){
                kbControl.addKhaiBao(kb);
            //}
        }
    }//GEN-LAST:event_jButton1ActionPerformed

