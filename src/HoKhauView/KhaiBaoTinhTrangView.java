    public void hienThiThongTinKhaiBao(TamTruTamVangModel khaibao, String soNha){
        this.CCCDText.setText(khaibao.getCCCD());
        this.diaDiemText.setText("");
        this.hoTenText.setText("");
        this.chuHoText.setText("");
        this.ngayBDText.setDate(khaibao.getBatDau());
        this.ngayKTText.setDate(khaibao.getKetThuc());
        
        this.loaiBox.setSelectedItem(khaibao.getLoaiDangKy()); 
        
        this.chuyenDenText.setText(khaibao.getDiaDiem());
        this.lidoText.setText(khaibao.getLido());
        this.diaDiemText.setText(soNha);
        if(!(kbControl.getNhanKhau(CCCD)== null)){
            this.hoTenText.setText(kbControl.getNhanKhau(CCCD).getHoTen());
        }else{
            JOptionPane.showMessageDialog(this, "Không tồn tại mã CCCD này !");
        }
        
        if(!(kbControl.getHoKhau(soNha)==null)){
            this.chuHoText.setText(kbControl.getHoKhau(soNha).getTenChu());
        }else{
            JOptionPane.showMessageDialog(this, "Không tồn tại số nhà này !");
        }
}
    
